    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM=table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
              
            
            @Override
            public void valueChanged(ListSelectionEvent e) {
               if(e.getValueIsAdjusting()){
                return;
            }
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Equipments eq = etm.getEquipments(selectedIndex);
                    idField.setText(eq.getId().toString());
                    pajisjetField.setText(eq.getPaisjet()); 
                    ccbm.setSelectedItem(eq.getClassesID());
                    classesIdCMB.repaint();
                    
                   Image img;
                   try {
                       
                //        img = ImageIO.read(new File(e.getPath()));
                       img = ImageIO.read(new File("images/33721.png"));
                        Image dimg = img.getScaledInstance(imageLabel.getWidth(), imageLabel.getHeight(),Image.SCALE_SMOOTH); // 
                        ImageIcon icon = new ImageIcon(dimg);
                        imageLabel.setIcon(icon);
                    } catch (IOException ex) {
                        Logger.getLogger(EquipementsForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
               }
                   
               
            }
        });
        
    }
