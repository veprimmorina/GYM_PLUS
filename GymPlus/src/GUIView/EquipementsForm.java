    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
      if(pajisjetField.getText().trim().isEmpty() || (Classes)classesIdCMB.getSelectedItem()==null){ 
          JOptionPane.showMessageDialog(this, "Plotesoni te gjitha fushat");
      }
      else
          try{
            int row = table.getSelectedRow();
            if(row == -1){
              Equipments e = new Equipments();

                if(inputname.equals("")){
                   Random rand = new Random();
                   int random = rand.nextInt(100000);
                   output = new File("images/"+random+".png");
                   ImageIO.write(ImageIO.read(input), "png", output);
                   filename = output.getName();
               // e.setPath("images/"+filename);
                }
            // l.setKategoriaID(Integer.parseInt(idField.getText()));
              e.setPaisjet(pajisjetField.getText());
              e.setClassesID((Classes)classesIdCMB.getSelectedItem());
              er.create(e);
            }else{
               Equipments e= etm.getEquipments(row);
                //Id nuk e lejojm me ndryshu vetem emertimin ose fielda tjere qe mundeni me pas
                e.setPaisjet(pajisjetField.getText());
                 e.setClassesID((Classes)classesIdCMB.getSelectedItem());
                er.edit(e);
            }
            //E krijojm ni metode per me i clear fieldat mbasi ti shtojme
            clear();
            loadTable();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        } catch (IOException ex) {
          Logger.getLogger(EquipementsForm.class.getName()).log(Level.SEVERE, null, ex);
      }  
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
         int row = table.getSelectedRow();
        if (row != -1) {
            Object[] ob = {"Po", "Jo"};
            int i = JOptionPane.showOptionDialog(this, "A deshironi ta fshini ?", "Fshirja",
                JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if (i == 0) {
                Equipments e= etm.getEquipments(row);
                try {
                    er.delete(e);
                } catch (CrudFormException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
                clear();
                loadTable();
            } else {
                clear();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

     private void clear() {
        table.clearSelection();

        idField.setText("");
        pajisjetField.setText("");
        classesIdCMB.setSelectedIndex(-1);
        classesIdCMB.repaint();
    }
